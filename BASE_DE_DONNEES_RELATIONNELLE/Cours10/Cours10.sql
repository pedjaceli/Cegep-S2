DROP TABLE IF EXISTS clients;
CREATE TABLE clients (
    id_client INT AUTO_INCREMENT,
    nom VARCHAR(100) NOT NULL,
    email VARCHAR(150) UNIQUE NOT NULL,
    date_inscription DATE NOT NULL,
    PRIMARY KEY (id_client)
) ENGINE=InnoDB;

DROP TABLE IF EXISTS produits;
CREATE TABLE produits (
    id_produit INT AUTO_INCREMENT,
    nom_produit VARCHAR(150) NOT NULL,
    prix DECIMAL(10,2) NOT NULL,
    stock INT NOT NULL DEFAULT 0,
    PRIMARY KEY (id_produit)
) ENGINE=InnoDB;

DROP TABLE IF EXISTS commandes;
CREATE TABLE commandes (
    id_commande INT AUTO_INCREMENT,
    id_client INT NOT NULL,
    date_commande DATETIME NOT NULL,
    PRIMARY KEY (id_commande),
    FOREIGN KEY (id_client) REFERENCES clients(id_client) ON DELETE CASCADE
) ENGINE=InnoDB;

DROP TABLE IF EXISTS jonctionCommandes;
CREATE TABLE jonctionCommandes (
    id_ligne INT AUTO_INCREMENT,
    id_commande INT NOT NULL,
    id_produit INT NOT NULL,
    quantite INT NOT NULL,
    PRIMARY KEY (id_ligne),
    FOREIGN KEY (id_commande) REFERENCES commandes(id_commande) ON DELETE CASCADE,
    FOREIGN KEY (id_produit) REFERENCES produits(id_produit) ON DELETE RESTRICT
) ENGINE=InnoDB;

