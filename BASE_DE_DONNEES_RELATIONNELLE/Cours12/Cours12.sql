DROP TABLE IF EXISTS Saisons;
CREATE TABLE Saisons (
    id_saison INT PRIMARY KEY,
    titre VARCHAR(100),
    annee_diffusion INT
);

DROP TABLE IF EXISTS Episodes;
CREATE TABLE Episodes (
    id_episode INT PRIMARY KEY,
    titre_episode VARCHAR(150),
    id_saison INT,
    FOREIGN KEY (id_saison) REFERENCES Saisons(id_saison)
);

DROP TABLE IF EXISTS Personnages;
CREATE TABLE Personnages (
    id_personnage INT PRIMARY KEY,
    nom VARCHAR(100),
    alter_ego VARCHAR(100)
);

DROP TABLE IF EXISTS Apparitions;
CREATE TABLE Apparitions (
    id_personnage INT,
    id_episode INT,
    role_majeur BOOLEAN,
    PRIMARY KEY (id_personnage, id_episode),
    FOREIGN KEY (id_personnage) REFERENCES Personnages(id_personnage),
    FOREIGN KEY (id_episode) REFERENCES Episodes(id_episode)
);

DROP TABLE IF EXISTS Produits_Derives;
CREATE TABLE Produits_Derives (
    id_produit INT PRIMARY KEY,
    nom_produit VARCHAR(150),
    id_personnage INT,
    FOREIGN KEY (id_personnage) REFERENCES Personnages(id_personnage)
);

